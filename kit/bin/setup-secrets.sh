#!/usr/bin/env bash
# kit/bin/setup-secrets.sh
#
# Collects every credential the king prompt (kit/docs/START_NEW.md) will need
# into a single .env.kit file at repo root. Run this BEFORE pasting the king
# prompt into Claude Code.
#
# Re-run safely: prompts to overwrite if .env.kit already exists.
#
# Fixes KIT_RETROSPECTIVE.md F2 (creds collected ad-hoc → momentum loss).

set -euo pipefail

# Resolve repo root. Script lives at <repo>/kit/bin/, so REPO_ROOT is two
# levels up. Works regardless of where the script is invoked from.
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
ENV_FILE="$REPO_ROOT/.env.kit"

# ---------- helpers ----------
prompt_value() {
  local label="$1"
  local var_name="$2"
  local example="${3:-}"
  local example_text=""
  [[ -n "$example" ]] && example_text=" (e.g. $example)"
  printf "  %s%s: " "$label" "$example_text"
  read -r value
  if [[ -z "$value" ]]; then
    echo "    ERROR: $label is required. Aborting."
    exit 1
  fi
  printf -v "$var_name" '%s' "$value"
}

prompt_secret() {
  local label="$1"
  local var_name="$2"
  local example="${3:-}"
  local example_text=""
  [[ -n "$example" ]] && example_text=" (e.g. $example)"
  printf "  %s%s: " "$label" "$example_text"
  read -rs value
  echo
  if [[ -z "$value" ]]; then
    echo "    ERROR: $label is required. Aborting."
    exit 1
  fi
  printf -v "$var_name" '%s' "$value"
}

# ---------- header ----------
echo
echo "================================================================"
echo "  monorepo-kit setup-secrets"
echo "================================================================"
echo
echo "  Collects all creds into .env.kit (at repo root, gitignored)."
echo "  Re-run any time to overwrite."
echo

# ---------- existing-file check ----------
if [[ -f "$ENV_FILE" ]]; then
  printf "  .env.kit already exists. Overwrite? [y/N] "
  read -r confirm
  if [[ "$confirm" != "y" && "$confirm" != "Y" ]]; then
    echo "  Cancelled. Existing .env.kit preserved."
    exit 0
  fi
fi

# ---------- pre-flight reminders ----------
echo "  Pre-flight checklist (do these BEFORE this script, not after):"
echo "    1. Convex project exists in dashboard:"
echo "       https://dashboard.convex.dev"
echo
echo "    2. Clerk app exists with the auth providers you want enabled:"
echo "       https://dashboard.clerk.com"
echo
echo "    3. Clerk JWT template named 'convex' is configured:"
echo "       Clerk dashboard -> JWT Templates -> + New template -> Convex (preset)"
echo "       Save it. Then copy the 'Issuer' URL — you'll paste it below."
echo
printf "  Have you done all three? [y/N] "
read -r preflight
if [[ "$preflight" != "y" && "$preflight" != "Y" ]]; then
  echo
  echo "  Stop. Do the three pre-flight items first, then re-run this script."
  exit 1
fi
echo

# ---------- collect ----------
echo "  --- App identity ---"
prompt_value "  Working slug (lowercase, hyphens)" WORKING_SLUG "celebrity-fitness"
echo

echo "  --- Convex ---"
echo "  Find these in your Convex dashboard URL:"
echo "    https://dashboard.convex.dev/t/<TEAM_SLUG>/<PROJECT_NAME>"
prompt_value "  Convex team slug" CONVEX_TEAM "steven-harris"
prompt_value "  Convex project name" CONVEX_PROJECT "celebrity-fitness"
echo

echo "  --- Clerk ---"
echo "  In Clerk dashboard -> API Keys, find:"
prompt_value  "  Clerk publishable key" CLERK_PK "pk_test_..."
prompt_secret "  Clerk secret key (hidden input)" CLERK_SK "sk_test_..."
echo
echo "  In Clerk dashboard -> JWT Templates -> 'convex' template:"
prompt_value "  Clerk JWT issuer URL" CLERK_ISSUER "https://your-name.clerk.accounts.dev"
echo

# ---------- sanity checks (warn, don't block) ----------
warned=0
if [[ ! "$CLERK_PK" =~ ^pk_(test|live)_ ]]; then
  echo "  WARNING: Clerk publishable key doesn't start with pk_test_ or pk_live_."
  warned=1
fi
if [[ ! "$CLERK_SK" =~ ^sk_(test|live)_ ]]; then
  echo "  WARNING: Clerk secret key doesn't start with sk_test_ or sk_live_."
  warned=1
fi
if [[ ! "$CLERK_ISSUER" =~ ^https:// ]]; then
  echo "  WARNING: Clerk issuer URL doesn't start with https://."
  warned=1
fi
if [[ ! "$WORKING_SLUG" =~ ^[a-z0-9-]+$ ]]; then
  echo "  WARNING: Working slug should be lowercase letters, numbers, hyphens only."
  warned=1
fi
if [[ "$warned" -eq 1 ]]; then
  printf "  Continue anyway? [y/N] "
  read -r override
  if [[ "$override" != "y" && "$override" != "Y" ]]; then
    echo "  Cancelled. Re-run when values are correct."
    exit 1
  fi
fi

# ---------- write ----------
cat > "$ENV_FILE" <<EOF
# .env.kit — generated by kit/bin/setup-secrets.sh
# Read by kit/docs/START_NEW.md and distributed to subapp .env files.
# DO NOT COMMIT (covered by .gitignore via .env*).

WORKING_SLUG=$WORKING_SLUG

CONVEX_TEAM=$CONVEX_TEAM
CONVEX_PROJECT=$CONVEX_PROJECT

NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY=$CLERK_PK
EXPO_PUBLIC_CLERK_PUBLISHABLE_KEY=$CLERK_PK
CLERK_SECRET_KEY=$CLERK_SK

CLERK_JWT_ISSUER_URL=$CLERK_ISSUER
EOF

chmod 600 "$ENV_FILE"

# ---------- footer ----------
echo
echo "  ----------------------------------------------------------------"
echo "  ✓ Wrote .env.kit ($(wc -l < "$ENV_FILE" | tr -d ' ') lines)"
echo "  ✓ Permissions set to 600 (owner read/write only)"
echo "  ----------------------------------------------------------------"
echo
echo "  Next: open Claude Code in this repo and paste the king prompt"
echo "  from kit/docs/START_NEW.md."
echo
